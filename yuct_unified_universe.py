# ЗАПУСК ТЕСТА ДЛЯ РАЗНЫХ ТЕМПЕРАТУРНЫХ РЕЖИМОВ
# ==============================================================================
print("=" * 75)
print(" БЕНЧМАРК YUCT: ПРОВЕРКА ТЕМПЕРАТУРНОЙ ЗАВИСИМОСТИ ГРАВИТАЦИИ G")
print("=" * 75)

# 1. Точка абсолютного нуля (Космический вакуум)
G_cold = get_temperature_dependent_G(0.0)

# 2. Комнатная температура (Условия земных лабораторий, ~20°C)
G_room = get_temperature_dependent_G(293.15)

# 3. Экстремальный нагрев (Ядро Солнца / Высокотемпературная плазма)
G_hot = get_temperature_dependent_G(1.5e7)

print(f"1. G при абсолютном нуле (0 К)     : {G_cold:.7e} м³/кг·с²")
print(f"2. G при комнатной температуре (293 К): {G_room:.7e} м³/кг·с²")
print(f"3. G в ядре Солнца (15 000 000 К)   : {G_hot:.7e} м³/кг·с²")
print("-" * 75)

# Расчет отклонений
diff_room = G_room - G_cold
diff_hot = G_hot - G_cold

print(f"Земное лабораторное смещение ΔG     : {diff_room:.2e} м³/кг·с²")
print(f"Экстремальное солнечное смещение ΔG  : {diff_hot:.2e} м³/кг·с²")
print("=" * 75)
