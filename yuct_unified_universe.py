import math
import time

# Базовые константы YUCT
M_ELECTRON = 9.1093837e-31       # Масса электрона (кг)
C_LIGHT = 299792458              # Скорость света (м/с)
H_PLANCK = 6.62607015e-34        # Постоянная Планка
Q_QUANTUM = (3 / 2) ** (1 / 3)  # Шаг решетки (q ≈ 1.1447)
PLANCK_NODE = 382.0              # Базовый Планковский узел

def get_temperature_dependent_G(T_kelvin: float):
    """
    Вычисляет гравитационную постоянную G с учетом температуры среды.
    T_kelvin = 0.0 — абсолютный ноль (чистая геометрия вакуума).
    """
    # Тепловая поправка к натяжению решетки.
    # При росте температуры "пиксели" пространства совершают флуктуации Фейгенбаума
    # Коэффициент k_thermal задает масштаб квантового шума
    k_thermal = 1.380649e-23  # Постоянная Больцмана для масштабирования энергии
    E_electron = M_ELECTRON * (C_LIGHT ** 2)
    
    # Динамический сдвиг Планковского узла под действием тепловой энергии
    thermal_shift = (k_thermal * T_kelvin) / E_electron
    dynamic_planck_node = PLANCK_NODE - thermal_shift
    
    # Расчет динамической массы Планка
    m_planck_dynamic = M_ELECTRON * (Q_QUANTUM ** dynamic_planck_node)
    
    # Вывод температурной константы Ньютона G
    h_bar = H_PLANCK / (2 * math.pi)
    g_newton_dynamic = (h_bar * C_LIGHT) / (m_planck_dynamic ** 2)
    
    return g_newton_dynamic

